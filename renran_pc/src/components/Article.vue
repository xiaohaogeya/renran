        },
      data(){
          return {
            token: "",
            article_id: 0,
            is_show_reward_window: false,
            article: {
              user: {},
              collection: {}
            },
            reward_list: [2, 5, 10, 20, 50, 100],
            pay_type_list: ["支付宝", "余额支付"],
            reward_info:{
              money: 2,
              content: "",
              pay_type: 0,
            },
          }
      },
      created(){
          this.token = this.$settings.check_user_login(this);
          this.article_id = this.$router.params.id;
          this.get_article();
      },
      filters:{
          timeformat(time){
            return time.split(".")[0].replace("T", " ")
          }
      },
      methods:{
          get_article(){
            // 获取文章信息
            this.$axios.get(`${this.$settings.Host}/article/${this.article_id}/`
            ).then(response=>{
              this.article = response.data;
            }).catch(error=>{
              this.$message.error("当前文章已删除");
              this.$router.go(-1);
            })
          },
        payhandler(){
          // 支付处理
          this.reward_info.article_id = this.article_id;
          this.$axios.post(`${this.$settings.Host}/payments/alipay/`,this.reward_info,{
            headers:{
              Authorization: "jwt" + this.token
            }
          }).then(response=>{
            location.href = response.data;
          }).catch(error=>{
            this.$message.error("发起打赏请求失败")
          })
        },
      }
